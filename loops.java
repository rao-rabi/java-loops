public class loops {
    public static void main(String[] args) {
    //for loop
        for(int i = 0; i<=10; i++ ){
            System.out.print(i + " ");
        }
    //while loop    
        int j= 0;
        while(j<11){
            System.out.println(j);
            j++;
        }
    //dowhile loop    
        int z = 0;
        do {
            System.out.print(z + " ");
            z++;
        } while (z<11);
    }
}
